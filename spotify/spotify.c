// ---------------------------  SESSION CALLBACKS  -------------------------

void logged_in(sp_session *session, sp_error error) {
	cspotify_logged_in();
}

void notify_main_thread(sp_session *session) {
	cspotify_notify_main_thread();
}

int music_delivery(sp_session *session, const sp_audioformat *format, const void *frames, int num_frames) {
	return cspotify_music_delivery();
}

void metadata_updated(sp_session *session) {
	cspotify_metadata_updated();
}

void play_token_lost(sp_session *session) {
	cspotify_play_token_lost();
}

void end_of_track(sp_session *session) {
	cspotify_end_of_track();
}

void log_message(sp_session *sessionion, const char *data)
{
	fprintf(stderr, "%s", data);
}

// -------------------------  END SESSION CALLBACKS  -----------------------

// The session callbacks.
static sp_session_callbacks session_callbacks = {
	.logged_in = (void*)logged_in,
	.notify_main_thread = (void*)notify_main_thread,
	.music_delivery = (void*)music_delivery,
	.metadata_updated = (void*)metadata_updated,
	.play_token_lost = (void*)play_token_lost,
	.log_message = (void*)log_message,
	.end_of_track = (void*)end_of_track,
};

// The session configuration.
static sp_session_config spconfig = {
	.api_version = SPOTIFY_API_VERSION,
	.cache_location = "tmp",
	.settings_location = "tmp",
	.application_key = g_appkey,
	.application_key_size = sizeof(g_appkey),
	.user_agent = "spotify-core-appname",
	.callbacks = &session_callbacks,
	NULL,
};

void cspotify_login(char *username, char *password) {
	sp_session *sp;
	sp_error err;

	printf("creating session\n");
	err = sp_session_create(&spconfig, &sp);
	if (err != SP_ERROR_OK) {
		fprintf(stderr, "failed to create session: %s\n", sp_error_message(err));
		return;
	}

	printf("logging in\n");
	char *blob = NULL;
	err = sp_session_login(sp, username, password, 1, blob);
	if (err != SP_ERROR_OK) {
		fprintf(stderr, "failed to login: %s\n", sp_error_message(err));
		return;
	}
}
